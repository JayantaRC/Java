
package com.mycompany.javatutorial;


public class StringUpperLower {
    
    public static void main (String[]args){
        
        //The Code will help to make "Hello, India!" to Cap "HELLO, INDIA!"
        String upper = "Hello, India!";
        System.out.println("Capital : "+upper.toUpperCase());
        
        
        //The Code will help to make "Hello, India!" to Small "hello, india!"
        String lower = "Hello, India!";
        System.out.println("Small : "+lower.toLowerCase());
    }
    
}
