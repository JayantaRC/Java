
package com.mycompany.javatutorial;


public class StringIndexOf {
    
    public static void main (String[]args){
        
        
        /* This method returns the index (the position) 
           of the first occurrence of a specified text 
           in a string (including whitespace) */
        
        String text = "My organisation is great";
        System.out.println("Find where the 'organisation' occurs! : "+text.indexOf("organisation"));
    }
    
}
