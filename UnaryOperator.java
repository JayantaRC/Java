
package com.mycompany.javatutorial;


public class ExampleUnary {
    
    public static void main (String [] args){
        
        int x = -10;
        int result;
        
        result = +x; //-10
        System.out.println("Result : "+result);
        
        result = -x;  //+10
        System.out.print("Result : "+result);
    }
    
}
