
package com.mycompany.javatutorial;


public class LoopFor {
    
    public static void main (String[]args){
        
        int number = 2;
        for(int i = 1; i<=10; i++){
            
             // Print the multiplication table for the number 2
            System.out.println(number + "x" + i + "=" + (number * i));
        }
        
    }
    
}
